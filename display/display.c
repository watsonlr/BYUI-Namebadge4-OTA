void display_set_madctl(uint8_t madctl)
{
    disp_cmd(0x36);
    disp_data(&madctl, 1);
}

void display_init(void)
{
    ESP_LOGI(TAG, "Initialising SPI2 and ILI9341 (landscape 320×240)");
    spi_and_gpio_init();
    ili9341_hw_reset();
    ili9341_init_regs();
    ESP_LOGI(TAG, "Display ready");
}

void display_fill(uint16_t color)
{
    /* Build a one-row pixel buffer (320 pixels × 2 bytes). */
    static uint8_t row_buf[DISPLAY_W * 2];
    for (int i = 0; i < DISPLAY_W; i++) {
        pack_pixel(&row_buf[i * 2], color);
    }

    set_window(0, 0, DISPLAY_W - 1, DISPLAY_H - 1);
    for (int y = 0; y < DISPLAY_H; y++) {
        write_pixels(row_buf, DISPLAY_W * 2);
    }
}

void display_fill_rect(int x, int y, int w, int h, uint16_t color)
{
    /* Clamp to screen bounds. */
    if (x < 0)              { w += x; x = 0; }
    if (y < 0)              { h += y; y = 0; }
    if (x + w > DISPLAY_W)  w = DISPLAY_W - x;
    if (y + h > DISPLAY_H)  h = DISPLAY_H - y;
    if (w <= 0 || h <= 0)   return;

    static uint8_t row_buf[DISPLAY_W * 2];
    for (int i = 0; i < w; i++) pack_pixel(&row_buf[i * 2], color);

    set_window((uint16_t)x, (uint16_t)y,
               (uint16_t)(x + w - 1), (uint16_t)(y + h - 1));
    for (int row = 0; row < h; row++) write_pixels(row_buf, w * 2);
}

void display_draw_char(int x, int y, char c, uint16_t fg, uint16_t bg, int scale)
{
    if (c < 0x20 || c > 0x7F) c = '?';
    const uint8_t *glyph = s_font8x8[(uint8_t)(c - 0x20)];

    /* Two-byte packed colour values for the SPI buffer. */
    uint8_t fg_hi = (uint8_t)(fg >> 8), fg_lo = (uint8_t)(fg & 0xFF);
    uint8_t bg_hi = (uint8_t)(bg >> 8), bg_lo = (uint8_t)(bg & 0xFF);

    /* One scaled row: 8 font columns × scale pixels × 2 bytes each. */
    int row_bytes = DISPLAY_FONT_W * scale * 2;
    uint8_t row_buf[DISPLAY_FONT_W * 4 * 2]; /* scale up to 4 fits here */

    for (int fr = 0; fr < DISPLAY_FONT_H; fr++) {
        uint8_t bits = glyph[fr];

        /* Build the pixel run for this scaled row. */
        int bi = 0;
        for (int fc = 0; fc < DISPLAY_FONT_W; fc++) {
            bool lit = (bits >> fc) & 1; /* bit 0 = leftmost column */
            uint8_t hi = lit ? fg_hi : bg_hi;
            uint8_t lo = lit ? fg_lo : bg_lo;
            for (int sx = 0; sx < scale; sx++) {
                row_buf[bi++] = hi;
                row_buf[bi++] = lo;
            }
        }

        /* Write scale identical rows for vertical scaling. */
        for (int sy = 0; sy < scale; sy++) {
            int screen_y = y + fr * scale + sy;
            if (screen_y >= DISPLAY_H) return;
            int screen_x1 = x + DISPLAY_FONT_W * scale - 1;
            if (screen_x1 >= DISPLAY_W) screen_x1 = DISPLAY_W - 1;
            set_window((uint16_t)x, (uint16_t)screen_y,
                       (uint16_t)screen_x1, (uint16_t)screen_y);
            write_pixels(row_buf, row_bytes);
        }
    }
}

void display_draw_string(int x, int y, const char *str,
                         uint16_t fg, uint16_t bg, int scale)
{
    int cx = x;
    while (*str) {
        display_draw_char(cx, y, *str, fg, bg, scale);
        cx += DISPLAY_FONT_W * scale;
        str++;
    }
}

/* ── Internal: draw one char from an arbitrary 8×8 font table ───── */
static void draw_char_font(int x, int y, char c,
                           uint16_t fg, uint16_t bg, int scale,
                           const uint8_t font[96][8])
{
    if (c < 0x20 || c > 0x7F) c = '?';
    const uint8_t *glyph = font[(uint8_t)(c - 0x20)];

    uint8_t fg_hi = (uint8_t)(fg >> 8), fg_lo = (uint8_t)(fg & 0xFF);
    uint8_t bg_hi = (uint8_t)(bg >> 8), bg_lo = (uint8_t)(bg & 0xFF);

    int row_bytes = DISPLAY_FONT_W * scale * 2;
    uint8_t row_buf[DISPLAY_FONT_W * 4 * 2];

    for (int fr = 0; fr < DISPLAY_FONT_H; fr++) {
        uint8_t bits = glyph[fr];
        int bi = 0;
        for (int fc = 0; fc < DISPLAY_FONT_W; fc++) {
            bool lit = (bits >> fc) & 1;
            uint8_t hi = lit ? fg_hi : bg_hi;
            uint8_t lo = lit ? fg_lo : bg_lo;
            for (int sx = 0; sx < scale; sx++) {
                row_buf[bi++] = hi;
                row_buf[bi++] = lo;
            }
        }
        for (int sy = 0; sy < scale; sy++) {
            int screen_y = y + fr * scale + sy;
            if (screen_y >= DISPLAY_H) return;
            int screen_x1 = x + DISPLAY_FONT_W * scale - 1;
            if (screen_x1 >= DISPLAY_W) screen_x1 = DISPLAY_W - 1;
            set_window((uint16_t)x, (uint16_t)screen_y,
                       (uint16_t)screen_x1, (uint16_t)screen_y);
            write_pixels(row_buf, row_bytes);
        }
    }
}

void display_print(const display_text_ctx_t *ctx, int x, int y, const char *str)
{
    const uint8_t (*font)[8] =
        (ctx->font == DISPLAY_FONT_SANS) ? s_font8x8_sans : s_font8x8;
    int cx = x;
    while (*str) {
        draw_char_font(cx, y, *str, ctx->fg, ctx->bg, ctx->scale, font);
        cx += DISPLAY_FONT_W * ctx->scale;
        str++;
    }
}

void display_draw_bitmap(int x, int y, int w, int h, const uint16_t *pixels)
{
    if (w <= 0 || h <= 0 || !pixels) return;
    if (x < 0 || y < 0 || x + w > DISPLAY_W || y + h > DISPLAY_H) return;

    /* Set the address window once; ILI9341 auto-advances after each row. */
    set_window((uint16_t)x, (uint16_t)y,
               (uint16_t)(x + w - 1), (uint16_t)(y + h - 1));

    /* The .bin pixel buffer stores each pixel as a little-endian uint16_t
     * (native host order on ESP32), so pixels[col] yields the correct
     * RGB565 value.  pack_pixel converts each to big-endian wire order
     * (high byte first) as the ILI9341 requires. */
    uint8_t row_buf[DISPLAY_W * 2];
    for (int row = 0; row < h; row++) {
        const uint16_t *src = pixels + row * w;
        for (int col = 0; col < w; col++) {
            pack_pixel(&row_buf[col * 2], src[col]);
        }
        write_pixels(row_buf, w * 2);
    }
}

void display_draw_row_raw(int x, int y, int w, const uint16_t *pixels)
{
    set_window((uint16_t)x, (uint16_t)y,
               (uint16_t)(x + w - 1), (uint16_t)y);
    spi_transaction_t t = {
        .length    = (size_t)w * 16,
        .tx_buffer = pixels,
        .user      = (void *)1,
    };
    ESP_ERROR_CHECK(spi_device_polling_transmit(s_spi, &t));
}

bool display_draw_qr(int cx, int cy, const char *text,
                     int module_px, uint16_t fg, uint16_t bg)
{
    /* QR buffer: worst-case version 10 (57×57 = 3249 bits = 407 bytes). */
    static uint8_t qr_buf[QR_BUFFER_LEN_MAX];
    int qr_size = 0;

    if (!qr_encode(text, qr_buf, &qr_size)) {
        ESP_LOGE("display", "QR encode failed for: %s", text);
        return false;
    }

    /* QR spec requires a 4-module quiet zone on every side. */
    const int QUIET = 4;
    int qz_px     = QUIET * module_px;
    int total_px  = (qr_size + 2 * QUIET) * module_px;

    int x0 = cx - total_px / 2;   /* top-left corner of quiet zone */
    int y0 = cy - total_px / 2;

    static uint8_t row_buf[DISPLAY_W * 2];
    uint8_t bg_hi = (uint8_t)(bg >> 8);
    uint8_t bg_lo = (uint8_t)(bg & 0xFF);

    /* Clipped screen x-range (same for every row) */
    int sx0    = (x0 < 0) ? 0 : x0;
    int sx1    = (x0 + total_px - 1 < DISPLAY_W) ? x0 + total_px - 1 : DISPLAY_W - 1;
    int skip   = (x0 < 0) ? (-x0 * 2) : 0;
    int nbytes = (sx1 - sx0 + 1) * 2;

    /* Build a solid-background row (used for quiet-zone rows) */
    int full_px = (total_px <= DISPLAY_W) ? total_px : DISPLAY_W;
    for (int i = 0; i < full_px * 2;) {
        row_buf[i++] = bg_hi;
        row_buf[i++] = bg_lo;
    }

    /* Send a pre-built row to one screen scanline */
#define SEND_ROW(sy)  do {                                          \
        int _sy = (sy);                                             \
        if (_sy >= 0 && _sy < DISPLAY_H && nbytes > 0) {           \
            set_window((uint16_t)sx0, (uint16_t)_sy,               \
                       (uint16_t)sx1, (uint16_t)_sy);              \
            write_pixels(row_buf + skip, nbytes);                  \
        }                                                           \
    } while (0)

    /* Top quiet zone */
    for (int py = 0; py < qz_px; py++) {
        SEND_ROW(y0 + py);
    }

    /* Data rows with left/right quiet margins */
    int data_y0 = y0 + qz_px;
    for (int row = 0; row < qr_size; row++) {
        int bi = 0;
        /* Left quiet zone */
        for (int i = 0; i < qz_px; i++) {
            row_buf[bi++] = bg_hi;
            row_buf[bi++] = bg_lo;
        }
        /* QR symbol modules */
        for (int col = 0; col < qr_size; col++) {
            bool dark      = qr_get_module(qr_buf, qr_size, col, row);
            uint16_t color = dark ? fg : bg;
            uint8_t hi     = (uint8_t)(color >> 8);
            uint8_t lo     = (uint8_t)(color & 0xFF);
            for (int p = 0; p < module_px; p++) {
                if (bi / 2 >= DISPLAY_W) break;
                row_buf[bi++] = hi;
                row_buf[bi++] = lo;
            }
        }
        /* Right quiet zone */
        for (int i = 0; i < qz_px && bi / 2 < DISPLAY_W; i++) {
            row_buf[bi++] = bg_hi;
            row_buf[bi++] = bg_lo;
        }

        /* Repeat this row module_px scanlines vertically */
        for (int py = 0; py < module_px; py++) {
            SEND_ROW(data_y0 + row * module_px + py);
        }
    }

    /* Bottom quiet zone — rebuild bg row (data rows overwrote it) */
    for (int i = 0; i < full_px * 2;) {
        row_buf[i++] = bg_hi;
        row_buf[i++] = bg_lo;
    }
    int bot_y0 = data_y0 + qr_size * module_px;
    for (int py = 0; py < qz_px; py++) {
        SEND_ROW(bot_y0 + py);
    }

#undef SEND_ROW
    return true;
}
