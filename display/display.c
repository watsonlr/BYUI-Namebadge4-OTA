static void ili9341_hw_reset(void)
{
    gpio_set_level(DISP_PIN_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(10));
    gpio_set_level(DISP_PIN_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(120));
}

static void ili9341_init_regs(void)
{
    disp_cmd(0x01); vTaskDelay(pdMS_TO_TICKS(150)); /* software reset */
    disp_cmd(0xEF); disp_data((uint8_t[]){0x03,0x80,0x02}, 3);
    disp_cmd(0xCF); disp_data((uint8_t[]){0x00,0xC1,0x30}, 3);
    disp_cmd(0xED); disp_data((uint8_t[]){0x64,0x03,0x12,0x81}, 4);
    disp_cmd(0xE8); disp_data((uint8_t[]){0x85,0x00,0x78}, 3);
    disp_cmd(0xCB); disp_data((uint8_t[]){0x39,0x2C,0x00,0x34,0x02}, 5);
    disp_cmd(0xF7); disp_data((uint8_t[]){0x20}, 1);
    disp_cmd(0xEA); disp_data((uint8_t[]){0x00,0x00}, 2);
    disp_cmd(0xC0); disp_data((uint8_t[]){0x23}, 1);   /* power control 1 */
    disp_cmd(0xC1); disp_data((uint8_t[]){0x10}, 1);   /* power control 2 */
    disp_cmd(0xC5); disp_data((uint8_t[]){0x3E,0x28}, 2); /* VCOM */
    disp_cmd(0xC7); disp_data((uint8_t[]){0x86}, 1);
    disp_cmd(0x36); disp_data((uint8_t[]){0x40}, 1);   /* MADCTL — landscape (MX) */  
    disp_cmd(0x3A); disp_data((uint8_t[]){0x55}, 1);   /* 16-bit colour */
    disp_cmd(0xB1); disp_data((uint8_t[]){0x00,0x18}, 2); /* frame rate ~70 Hz */
    disp_cmd(0xB6); disp_data((uint8_t[]){0x08,0x82,0x27}, 3);
    disp_cmd(0xF2); disp_data((uint8_t[]){0x00}, 1);
    disp_cmd(0x26); disp_data((uint8_t[]){0x01}, 1);
    disp_cmd(0xE0); disp_data((uint8_t[]){0x0F,0x31,0x2B,0x0C,0x0E,0x08,
                                           0x4E,0xF1,0x37,0x07,0x10,0x03,
                                           0x0E,0x09,0x00}, 15);
    disp_cmd(0xE1); disp_data((uint8_t[]){0x00,0x0E,0x14,0x03,0x11,0x07,
                                           0x31,0xC1,0x48,0x08,0x0F,0x0C,
                                           0x31,0x36,0x0F}, 15);
    disp_cmd(0x11); vTaskDelay(pdMS_TO_TICKS(120)); /* sleep out */
    disp_cmd(0x21);                                  /* inversion on — panel default is inverted */
    disp_cmd(0x29);                                  /* display on */
}

static void spi_and_gpio_init(void)
{
    gpio_config_t io = {
        .pin_bit_mask = (1ULL << DISP_PIN_DC) | (1ULL << DISP_PIN_RST),
        .mode         = GPIO_MODE_OUTPUT,
    };
    ESP_ERROR_CHECK(gpio_config(&io));

    spi_bus_config_t bus = {
        .mosi_io_num     = DISP_PIN_MOSI,
        .miso_io_num     = DISP_PIN_MISO,
        .sclk_io_num     = DISP_PIN_CLK,
        .quadwp_io_num   = -1,
        .quadhd_io_num   = -1,
        .max_transfer_sz = DISPLAY_W * 2 + 8,
    };
    ESP_ERROR_CHECK(spi_bus_initialize(DISP_SPI_HOST, &bus, SPI_DMA_CH_AUTO));

    spi_device_interface_config_t dev = {
        .clock_speed_hz = DISP_SPI_FREQ,
        .mode           = 0,
        .spics_io_num   = DISP_PIN_CS,
        .queue_size     = 7,
        .pre_cb         = spi_pre_transfer_cb,
    };
    ESP_ERROR_CHECK(spi_bus_add_device(DISP_SPI_HOST, &dev, &s_spi));
}

